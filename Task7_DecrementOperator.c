#include<stdio.h>
int main(void)
{
	int iNo =10;
	printf("\n%d",iNo);
	printf("\n%d",iNo--);
	printf("\n%d",iNo);
	printf("\n%d",--iNo);
	printf("\n%d",iNo);
}